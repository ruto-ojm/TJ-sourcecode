    /// ログイン処理や管理者機能において登録済みユーザーを一元管理する。
    /// </summary>
    public static class UserManager
    {
        /// <summary>
        /// 登録済みのユーザーオブジェクトの一覧を保持するコレクション。
        /// ・ログイン認証時に参照される。
        /// ・管理者画面などでユーザー一覧表示や削除に使用される。
        /// ・アプリ全体で共有されるため、どのフォームからもアクセス可能。
        /// </summary>
        public static List<User> Users { get; } = new List<User>();
    }
}

